// GETTING SUBSTRING FROM A STRING	

// Using the slice() method

let text = 'Hello World ';
console.log(text.slice(6)); // World

// Using the substring() method
text = 'Hello World, I am a JavaScript developer';
console.log(text.substring(13)); // I am a JavaScript developer

// Using the substr() method
text = 'Hello World, I am a JavaScript developer. I am learning JavaScript';
console.log(text.substr(42)); // I am learning JavaScript