// If conditional

let x = 'Bread';
let y = 'Morning';

if (x == 'Bread' && y == 'Morning') {
	  console.log('Good Morning!');
	}

// IF ELSE conditional

if (x !== 'Bread' || y !== 'Morning') {
	  console.log('Good Morning!');
	} else {
		console.log('It is not morning');
	}

// IF ELSE IF conditional

if (x == 'Bread' && y == 'Evening') {
	  console.log('Good Morning!');
	}
	else if (x !== 'Bread' && y == 'Afternoon') {
		console.log('Good Afternoon!');
	}
	else {
		console.log('Good Evening!');
	}
