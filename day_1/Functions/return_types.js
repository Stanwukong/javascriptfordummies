// Function that returns undefined
function test1() {
	return;
}
console.log(test1()); // undefined

// Function that returns a string
function test2() {
	return 'Hello World';
}
console.log(test2()); // Hello World

// Function that returns a number
function test3() {
	return 1;
}
console.log(test3()); // 1

// Function that returns a NULL value
function test4() {
	return null;
}
console.log(test4()); // null

