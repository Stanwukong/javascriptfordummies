// IF STATEMENTS

let age = 20;

if (age > 20){
	console.log('You are over 20 years old');
}

// ELSE STATEMENTS

if (age > 20){
	console.log('You are over 20 years old');
}
else {
	console.log('You are not over 20 years old');
}

// ELSE IF STATEMENTS

if (age > 20){
	console.log('You are over 20 years old');
}
else if (age > 30){
	console.log('You are over 30 years old');
}
else {
	console.log('You are not over 20 years old');
}
