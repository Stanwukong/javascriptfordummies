// A Basic If Statement

let pastry = 'bread';

if (pastry === 'bread') {
	console.log('Thanks for the bread');
}

// A Basic If Else Statement

if (pastry === 'cake') {
	console.log('Thanks for the cake');
} else {
	console.log('I asked for cake');
}

// A Basic If Else If Statement

if (pastry === 'cake') {
	console.log('Thanks for the cake');
}
else if (pastry === 'croissant') {
	console.log('Thanks for the bread');
}
else {
	console.log("Where's the bread?");
}